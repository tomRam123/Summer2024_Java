package Java_Classes;

public class JavaOperator_AdvancedAssignment {
    public static void main(String[] args) {
        /**
         * Advance assignment ( +=,-=,*=,/=,%= )
         *
         */

        int num = 5;
        num+=5;     // num = 10
        System.out.println(num);
        num*=2;     // num = 20
        System.out.println(num);
        num/=5;     // num = 4
        System.out.println(num);
        num%=2;     // num = 0
        System.out.println(num);

    }
}
